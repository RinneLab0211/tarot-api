# 正位置・逆位置をランダムで決める
positions = ["正位置", "逆位置"]

@app.route('/draw', methods=['GET'])
def draw_tarot():
    card = random.choice(tarot_deck)
    position = random.choice(positions)
    # ensure_ascii=False を追加して文字化けを防ぐ
    return jsonify({"card": card, "position": position}, ensure_ascii=False)

